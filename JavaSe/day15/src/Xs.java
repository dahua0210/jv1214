
public class Xs {
	private String name;
	private int num;
	private int score;
	public Xs(String name, int num, int score) {
		super();
		this.name = name;
		this.num = num;
		this.score = score;
	}
	public String getName() {
		return name;
	}
	
	public int getNum() {
		return num;
	}
	
	public int getScore() {
		return score;
	}
	
}
