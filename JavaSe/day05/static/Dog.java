
//抽象的狗
//某种品中的狗                            

public class Dog{
	//属性
	static String type; //类变量   不依赖于对象[类名.类变量]//品种
	
	int age;           //实例变量;依赖于对象 [对像引用.实例变量]
	String color;
	//静态代码块
	static{//使命:初始化 类变量
		
		type = "xxxx";
		System.out.println("我是在static代码中");
		//age = "12";  //!实例变量
		//color = "red";
	
	}
	
	
	
	//方法
	Dog(){
	
	}
	Dog(int age,String color){
		this.type = "哈士奇";
		this.age = age;
		this.color = color;	
	}
	//实例方法:this代表调用这的引用
	void bark(){
	this.type = "泰迪";
	System.out.println("barking...."+age+"年龄");
	}
	//类方法:不能访问[实例变量]===?不依赖于对象,
	static void bark1(){//没有对象,所以不能访问实例变量,实例方法
		//只能访问类变量,以及他的局部变量
		type = "京巴";
	}
}